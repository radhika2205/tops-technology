# TASK 3 — Rotation (45° & 90°) Without Cropping Corners
import cv2
import numpy as np
import matplotlib.pyplot as plt

img_bgr = images["fruits.jpg"]  # Task 1 wala images dict
H, W    = img_bgr.shape[:2]

# ── Helper function — canvas expand karo taaki corners crop na hon ──
def rotate_no_crop(image, angle_deg):
    h, w    = image.shape[:2]
    cx, cy  = w / 2, h / 2

    # Rotation matrix compute karo
    M = cv2.getRotationMatrix2D((cx, cy), angle_deg, 1.0)

    cos_a = abs(M[0, 0])
    sin_a = abs(M[0, 1])

    # Naya canvas size calculate karo
    new_w = int(h * sin_a + w * cos_a)
    new_h = int(h * cos_a + w * sin_a)

    # Matrix shift karo — centre adjust karo naye canvas ke liye
    M[0, 2] += (new_w / 2) - cx
    M[1, 2] += (new_h / 2) - cy

    rotated = cv2.warpAffine(image, M, (new_w, new_h),
                             flags=cv2.INTER_CUBIC,
                             borderMode=cv2.BORDER_CONSTANT,
                             borderValue=(255, 255, 255))
    return rotated, M

# Rotate karo
rot45,  M45  = rotate_no_crop(img_bgr,  45)
rot90,  M90  = rotate_no_crop(img_bgr,  90)
rot_n45, _   = rotate_no_crop(img_bgr, -45)

print(f" Original size   : {W}×{H}")
print(f" After  45°      : {rot45.shape[1]}×{rot45.shape[0]}")
print(f" After  90°      : {rot90.shape[1]}×{rot90.shape[0]}")
print(f" After -45°      : {rot_n45.shape[1]}×{rot_n45.shape[0]}")
print(f"\n Rotation Matrix (45°):\n{M45}")

# Visualise
fig, axes = plt.subplots(1, 4, figsize=(18, 5))
fig.patch.set_facecolor("#0f0f1a")
fig.suptitle("TASK 3 — Rotation via Transformation Matrix (No Corner Cropping)",
             color="white", fontsize=12, fontweight="bold")

imgs   = [img_bgr, rot45, rot90, rot_n45]
titles = ["Original", "Rotated 45°", "Rotated 90°", "Rotated -45°"]

for ax, im, t in zip(axes, imgs, titles):
    ax.imshow(cv2.cvtColor(im, cv2.COLOR_BGR2RGB))
    ax.set_title(t, color="white", fontsize=10)
    ax.axis("off")

plt.tight_layout()
plt.show()

