hsv_data   = [H_ch,      S_ch,          V_ch]
hsv_labels = ["Hue",     "Saturation",  "Value"]
hsv_colors = ["#ff9500", "#ff3399",     "#33ccff"]

for i, (ch, lbl, clr) in enumerate(zip(hsv_data, hsv_labels, hsv_colors)):
    ax = fig.add_subplot(gs[2, i + 1])
    ax.fill_between(range(256), channel_hist(ch), color=clr, alpha=0.75)
    ax.set_title(f"HSV — {lbl} Histogram", color="white", fontsize=8)
