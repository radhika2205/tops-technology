# TASK 4 — ROI Crop & Horizontal / Vertical Flips
import cv2
import numpy as np
import matplotlib.pyplot as plt

img_bgr = images["baboon.jpg"]  # Task 1 wala images dict
H, W    = img_bgr.shape[:2]

# ── ROI — central 40% region crop karo ──
y1 = int(H * 0.30);  y2 = int(H * 0.70)
x1 = int(W * 0.30);  x2 = int(W * 0.70)

roi = img_bgr[y1:y2, x1:x2].copy()

# ── Flips ──
flip_h = cv2.flip(roi,  1)   # flipCode=1  → Horizontal flip
flip_v = cv2.flip(roi,  0)   # flipCode=0  → Vertical flip
flip_b = cv2.flip(roi, -1)   # flipCode=-1 → Both axes flip

print(f" Original size   : {W}×{H}")
print(f"  ROI coords      : x=[{x1}, {x2}]  y=[{y1}, {y2}]")
print(f" ROI size        : {roi.shape[1]}×{roi.shape[0]}")
print(f"  Flip H (code=1) : {flip_h.shape}")
print(f"  Flip V (code=0) : {flip_v.shape}")
print(f"  Flip B (code=-1): {flip_b.shape}")

# ROI rectangle original image pe draw karo (context ke liye)
viz_orig = img_bgr.copy()
