# TASK 2 — Resizing & Pixel-Level Difference
import cv2
import numpy as np
import matplotlib.pyplot as plt

img_bgr = images["lena.jpg"]  # Task 1 wala images dict use hoga
img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)
H, W    = img_bgr.shape[:2]

# Down-scale then Up-scale (4x down to show interpolation difference clearly)
small_h, small_w = H // 4, W // 4

small_linear = cv2.resize(img_bgr, (small_w, small_h), interpolation=cv2.INTER_LINEAR)
small_cubic  = cv2.resize(img_bgr, (small_w, small_h), interpolation=cv2.INTER_CUBIC)

up_linear = cv2.resize(small_linear, (W, H), interpolation=cv2.INTER_LINEAR)
up_cubic  = cv2.resize(small_cubic,  (W, H), interpolation=cv2.INTER_CUBIC)

# Pixel-level absolute difference
diff      = cv2.absdiff(up_linear, up_cubic)
diff_norm = cv2.normalize(diff, None, 0, 255, cv2.NORM_MINMAX)

# MSE & PSNR calculate karo
mse  = np.mean((up_linear.astype(float) - up_cubic.astype(float)) ** 2)
psnr = 10 * np.log10(255**2 / mse) if mse > 0 else float("inf")

print(f" Original size   : {W}×{H}")
print(f" Downscaled to   : {small_w}×{small_h}")
print(f" Upscaled back   : {W}×{H}")
print(f" MSE  (L vs C)   : {mse:.4f}")
print(f" PSNR (L vs C)   : {psnr:.2f} dB")

# Visualise
fig, axes = plt.subplots(1, 4, figsize=(18, 5))
fig.patch.set_facecolor("#0f0f1a")
fig.suptitle("TASK 2 — INTER_LINEAR vs INTER_CUBIC Interpolation",
             color="white", fontsize=13, fontweight="bold")

panels = [
    (img_rgb,                                     "Original"),
    (cv2.cvtColor(up_linear, cv2.COLOR_BGR2RGB),  "INTER_LINEAR\n(upscaled)"),
    (cv2.cvtColor(up_cubic,  cv2.COLOR_BGR2RGB),  "INTER_CUBIC\n(upscaled)"),
    (cv2.cvtColor(diff_norm, cv2.COLOR_BGR2RGB),  f"Pixel Difference\nMSE={mse:.3f}  PSNR={psnr:.1f}dB"),
]

for ax, (panel_img, title) in zip(axes, panels):
    ax.imshow(panel_img)
    ax.set_title(title, color="white", fontsize=10)
    ax.axis("off")

plt.tight_layout()
plt.show()

