# TASK 5 — Color Space Conversion Module (BGR → Grayscale & HSV)
import cv2
import numpy as np
import matplotlib.pyplot as plt
import matplotlib.gridspec as gridspec

# ── Conversion function ──
def convert_color_spaces(bgr_image):
    gray = cv2.cvtColor(bgr_image, cv2.COLOR_BGR2GRAY)
    hsv  = cv2.cvtColor(bgr_image, cv2.COLOR_BGR2HSV)
    return gray, hsv

# ── Saari images pe conversion apply karo ──
print("=" * 55)
print("  TASK 5 — Color Space Analysis")
print("=" * 55)

for fname, img_bgr in images.items():
    gray, hsv    = convert_color_spaces(img_bgr)
    H_ch, S_ch, V_ch = cv2.split(hsv)

    print(f"\n    {fname}")
    print(f"      BGR  shape : {img_bgr.shape}")
    print(f"      Gray shape : {gray.shape}  (single channel)")
    print(f"      HSV  shape : {hsv.shape}")
    print(f"      H channel  : mean={H_ch.mean():.1f}, std={H_ch.std():.1f}")
    print(f"      S channel  : mean={S_ch.mean():.1f}, std={S_ch.std():.1f}")
    print(f"      V channel  : mean={V_ch.mean():.1f}, std={V_ch.std():.1f}")

# ── Detailed visualisation — lena.jpg ──
img_bgr      = images["lena.jpg"]
img_rgb      = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)
gray, hsv    = convert_color_spaces(img_bgr)
H_ch, S_ch, V_ch = cv2.split(hsv)
B, G, R          = cv2.split(img_bgr)

# Histogram helper
def channel_hist(channel):
    return cv2.calcHist([channel], [0], None, [256], [0, 256]).flatten()

# ── Figure layout ──
fig = plt.figure(figsize=(20, 10), facecolor="#0f0f1a")
fig.suptitle("TASK 5 — Color Space Conversion: BGR → Grayscale & HSV\nSource: lena.jpg",
             color="white", fontsize=13, fontweight="bold")

gs = gridspec.GridSpec(3, 5, figure=fig, hspace=0.45, wspace=0.3)

def show(ax, im, cmap=None, title=""):
    ax.imshow(im, cmap=cmap)
    ax.set_title(title, color="white", fontsize=9)
    ax.axis("off")

# Row 0 — Converted images
show(fig.add_subplot(gs[0, 0]), img_rgb,             title="BGR Source\n(RGB view)")
show(fig.add_subplot(gs[0, 1]), gray,  cmap="gray",  title="Grayscale")
show(fig.add_subplot(gs[0, 2]), H_ch,  cmap="hsv",   title="HSV — H\n(Hue)")
show(fig.add_subplot(gs[0, 3]), S_ch,  cmap="YlOrRd",title="HSV — S\n(Saturation)")
show(fig.add_subplot(gs[0, 4]), V_ch,  cmap="gray",  title="HSV — V\n(Value/Brightness)")

# Row 1 — BGR channels + histograms
show(fig.add_subplot(gs[1, 0]), R, cmap="Reds",   title="BGR — R channel")
show(fig.add_subplot(gs[1, 1]), G, cmap="Greens", title="BGR — G channel")
show(fig.add_subplot(gs[1, 2]), B, cmap="Blues",  title="BGR — B channel")

ax_gray_hist = fig.add_subplot(gs[1, 3])
ax_gray_hist.fill_between(range(256), channel_hist(gray), color="#aaaaaa", alpha=0.8)
ax_gray_hist.set_title("Gray Histogram", color="white", fontsize=9)
ax_gray_hist.tick_params(colors="white", labelsize=7)
